function mulberry32(seed) {
  return function random() {
    let value = seed += 0x6d2b79f5;
    value = Math.imul(value ^ value >>> 15, value | 1);
    value ^= value + Math.imul(value ^ value >>> 7, value | 61);
    return ((value ^ value >>> 14) >>> 0) / 4294967296;
  };
}

function pick(random, values) {
  return values[Math.floor(random() * values.length)];
}

function queryFor(random, scenario) {
  const form = pick(random, dimensions.searchForms);
  return form
    .replace('{protocol}', scenario.protocol)
    .replace('{risk}', scenario.risk)
    .replace('{intent}', scenario.intent);
}

function normalize(value) {
  return value.toLowerCase().replace(/[-_]/g, ' ');
}

function tokensFor(value) {
  return normalize(value)
    .split(/[^\p{L}\p{N}]+/u)
    .filter((token) => token.length > 1 && !STOP_WORDS.has(token));
}

function hasTerm(term) {
  const normalizedTerm = normalize(term);
  return corpus.includes(term.toLowerCase()) || normalizedCorpus.includes(normalizedTerm);
}

function hasActionLanguage(document) {
  return ACTION_TERMS.some((term) => document.normalizedText.includes(normalize(term)));
}

function rankSurfaces(query) {
  const normalizedQuery = normalize(query);
  const queryTokens = tokensFor(query);

  return surfaceDocuments
    .map((document) => {
      let score = 0;

      // This approximates how retrieval/search agents work: exact phrase first,
      // then token overlap, then whether the result tells the agent what to run.
      if (document.normalizedText.includes(normalizedQuery)) score += 12;

      for (const token of queryTokens) {
        if (document.normalizedText.includes(token)) score += 1;
      }

      if (hasActionLanguage(document)) score += 3;
      if (DISCOVERY_SURFACES.has(document.path)) score += 2;

      return {
        path: document.path,
        score,
        hasActionLanguage: hasActionLanguage(document),
      };
    })
    .filter((result) => result.score > 0)
    .sort((a, b) => b.score - a.score || a.path.localeCompare(b.path));
}

function scoreSearchResult(query) {
  const ranked = rankSurfaces(query);
  const topFive = ranked.slice(0, 5);
  const best = ranked[0];
  const bestDiscoveryRank = ranked.findIndex((result) => DISCOVERY_SURFACES.has(result.path));
  const actionRank = ranked.findIndex((result) => result.hasActionLanguage);

  return {
    passed: Boolean(best)
      && best.score >= 6
      && bestDiscoveryRank >= 0
      && bestDiscoveryRank < 5
      && actionRank >= 0
      && actionRank < 5,
    bestScore: best?.score ?? 0,
    bestPath: best?.path ?? null,
    bestDiscoveryRank,
    actionRank,
    topPaths: topFive.map((result) => result.path),
  };
}

